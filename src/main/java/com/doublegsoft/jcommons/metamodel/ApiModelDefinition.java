package com.doublegsoft.jcommons.metamodel;

/**
 * The {@link ApiModelDefinition} is a base type for any other api-style models.
 * 
 * @author <a href="mailto:guo.guo.gan@gmail.com">Christian Gann</a>
 * 
 * @since 3.0
 */
public abstract class ApiModelDefinition {
    
    protected String action;
    
    /**
     * the identifier for this model, and may be {@code null}.
     */
    protected String id;
    
    protected String body;

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
    
}
